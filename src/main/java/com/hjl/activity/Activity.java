public class Activity {
	
	private String type;
	
	private String keyword;
	
	public String getKeyword() {
		return keyword;
	}
	
	public Activity setKeyword(String keyword) {
		this.keyword = keyword;
		return this;
	}
	
	public String getType() {
		return type;
	}
	
	public Activity setType(String type) {
		this.type = type;
		return this;
	}
}
